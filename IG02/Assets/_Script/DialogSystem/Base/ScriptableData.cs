using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public abstract class ScriptableData< T > where T : ScriptableData< T >
{
	// -----------------------------------------------
	// 内部类
	// -----------------------------------------------
	// -----------------------------------------------
	// 枚举
	// -----------------------------------------------
	// -----------------------------------------------
	// 定义值
	// -----------------------------------------------
	// -----------------------------------------------
	// 公共变量
	// -----------------------------------------------
	// -----------------------------------------------
	// 成员变量
	// -----------------------------------------------
	public int Id;
	// -----------------------------------------------
	// 函数
	// -----------------------------------------------
#if UNITY_EDITOR
	/// <summary>
	/// 拷贝
	/// </summary>
	/// <typeparam name="T"></typeparam>
	/// <param name="from"></param>
	public virtual void CopyFrom( T from )
	{
		Id = from.Id;
	}
#endif
	// -----------------------------------------------
	// 属性
	// -----------------------------------------------
}

