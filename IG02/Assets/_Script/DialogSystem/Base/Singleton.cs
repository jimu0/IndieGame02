using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public abstract class Singleton< T > where T : class, new()
{
	// -----------------------------------------------
	// 内部类
	// -----------------------------------------------
	// -----------------------------------------------
	// 枚举
	// -----------------------------------------------
	// -----------------------------------------------
	// 定义值
	// -----------------------------------------------
	// -----------------------------------------------
	// 公共变量
	// -----------------------------------------------
	// -----------------------------------------------
	// 成员变量
	// -----------------------------------------------
	static T m_Instance = default;
	// -----------------------------------------------
	// 函数
	// -----------------------------------------------

	// -----------------------------------------------
	// 属性
	// -----------------------------------------------
	/// <summary>
	/// 实例
	/// </summary>
	public static T Instance
	{
		get
		{
			if( m_Instance == null )
			{
				m_Instance = new T();
			}
			return m_Instance;
		}
	}
}
