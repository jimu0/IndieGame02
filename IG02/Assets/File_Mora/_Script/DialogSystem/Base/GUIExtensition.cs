using System.Collections;
using UnityEngine;
using UnityEditor;
using System;

public class ColorScope : GUI.Scope
{
    // -----------------------------------------------
    // 内部类
    // -----------------------------------------------
    // -----------------------------------------------
    // 枚举
    // -----------------------------------------------
    // -----------------------------------------------
    // 定义值
    // -----------------------------------------------
    // -----------------------------------------------
    // 公共变量
    // -----------------------------------------------
    // -----------------------------------------------
    // 成员变量
    // -----------------------------------------------
    // -----------------------------------------------
    // 函数
    // -----------------------------------------------
    public ColorScope( Color color )
    {
        GUI.color = color;
    }

    /// <summary>
    /// 关闭范围
    /// </summary>
    protected override void CloseScope()
    {
        GUI.color = Color.white;
    }

}